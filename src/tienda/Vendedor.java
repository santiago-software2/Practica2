public class Vendedor {
    public String nombre;
    public String apellido;
    public int id;
    public String cargo;
    public int telefono;

    public Vendedor() {
    }

    public Vendedor(String nombre, String apellido, int id, String cargo, int telefono) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.id = id;
        this.cargo = cargo;
        this.telefono = telefono;
    }
    
    
}

